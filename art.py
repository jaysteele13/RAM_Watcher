def print_graphic(graphic, delay=0.05):
    lines = graphic.strip().split('\n')
    for line in lines:
        print(line)
        time.sleep(delay)

def create_separator(title, *lines):
    # Define the width of the separator
    width = 45  # Adjust this width to fit your title and lines
    # Format the title to fit within the separator
    title_line = f"* {title} *".center(width - 2)
    
    # Create the separator lines, supporting sub-lines
    lines_formatted = []
    for line in lines:
        if isinstance(line, tuple):  # Check if the line is a tuple containing a main line and sub-lines
            main_line = line[0]
            sub_lines = line[1:]

            # Format main line
            lines_formatted.append(f"* {main_line.center(width - 4)} *")

            # Format sub-lines with an arrow and indented
            for sub_line in sub_lines:
                lines_formatted.append(f"*{' '*14}--> {sub_line.ljust(width - 21)} *")
        else:
            # Format single line
            lines_formatted.append(f"* {line.center(width - 4)} *")

    # Create the full separator
    separator = f"""
{'*' * width}
*{' ' * (width - 2)}*
*{title_line}*
*{' ' * (width - 2)}*
{('\n'.join(lines_formatted)).rstrip()}
*{' ' * (width - 2)}*
{'*' * width}
"""
    
    return separator


